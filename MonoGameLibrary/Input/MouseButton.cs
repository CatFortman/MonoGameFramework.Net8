namespace MonoGameLibrary.Input;

/// <summary>
/// Defines the available mouse buttons.
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// The left mouse button.
    /// </summary>
    Left,

    /// <summary>
    /// The middle mouse button, typically the scroll wheel button.
    /// </summary>
    Middle,

    /// <summary>
    /// The right mouse button.
    /// </summary>
    Right,

    /// <summary>
    /// The first extended mouse button.
    /// </summary>
    XButton1,

    /// <summary>
    /// The second extended mouse button.
    /// </summary>
    XButton2,
}
